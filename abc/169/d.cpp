vector<ll> pfact(ll x) {
    vector<ll> res;
    for(ll i = 2; i * i <= x; i++) {
        while(x % i == 0) {
            x /= i;
            res.push_back(i);
        }
    }
    if(x != 1) res.push_back(x);
    return res;
}

